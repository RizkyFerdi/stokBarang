    private void btn_cariActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_cariActionPerformed
        // TODO add your handling code here:
    
        try {
            String nama = txtCari.getText();
            String sql = "SELECT * FROM barang WHERE nama = ?";
            java.sql.Connection conn = (Connection) Config.configDB();
            java.sql.PreparedStatement pst = conn.prepareStatement(sql);

            pst.setString(1, nama);

            java.sql.ResultSet rs = pst.executeQuery();

            if (nama.isEmpty()) {
                JOptionPane.showMessageDialog(null, "Nama is required");
            }
           else {
                boolean notFound = true; // Inisialisasi notFound

                while (rs.next()) {
                    txtNama.setText(rs.getString("nama"));
                    txtJumlah.setText(String.valueOf(rs.getInt("jumlah")));  
                    txtHarga.setText(String.valueOf(rs.getDouble("harga"))); 
                    txtKategori.setText(rs.getString("kategori"));
                    txtTanggalMasuk.setText(rs.getString("tanggal_masuk"));
                    txtTanggalKeluar.setText(rs.getString("tanggal_keluar"));
                    notFound = false; 
                }

                if (notFound) {
                    JOptionPane.showMessageDialog(null, "Nama tidak ditemukan");
                }
            }

            } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Gagal menampilkan data: " + e.getMessage());
        }
    }//GEN-LAST:event_btn_cariActionPerformed

