    public class RandomIdGenerator {
        private static final String CHARACTERS = "ABCDEFGHIJKLMNOPQRSTUVWXYZabcdefghijklmnopqrstuvwxyz0123456789";
        private static final int ID_LENGTH = 10;
        private static final SecureRandom RANDOM = new SecureRandom();

        public static String generateRandomId() {
            StringBuilder id = new StringBuilder(ID_LENGTH);
            for (int i = 0; i < ID_LENGTH; i++) {
                int index = RANDOM.nextInt(CHARACTERS.length());
                id.append(CHARACTERS.charAt(index));
            }
            return id.toString();
        }

        public static void main(String[] args) {
            String randomId = generateRandomId();
            System.out.println("Generated Random ID: " + randomId);
        }
    }

    
    public void insertData(String nama, int jumlah, double harga, String kategori, String tanggalMasuk, String tanggalKeluar) {
        try {
        
        String id_barang = RandomIdGenerator.generateRandomId();

        // Query SQL, tambahkan kolom 'id_barang'
        String sql = "INSERT INTO barang (id_barang, nama, jumlah, harga, kategori, tanggal_masuk, tanggal_keluar) VALUES (?, ?, ?, ?, ?, ?, ?)";
         java.sql.Connection conn = (Connection)Config.configDB();
         java.sql.PreparedStatement pst = conn.prepareStatement(sql);

        // Set nilai parameter query
        pst.setString(1, id_barang); 
        pst.setString(2, nama);
        pst.setInt(3, jumlah);
        pst.setDouble(4, harga);
        pst.setString(5, kategori);
        pst.setString(6, (tanggalMasuk)); 
        pst.setString(7, (tanggalKeluar));

        pst.execute();
        JOptionPane.showMessageDialog(null, "Data berhasil ditambahkan");
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Gagal menambahkan data: " + e.getMessage());
        }
    }
       
    public void readData() {
        DefaultTableModel tb = new DefaultTableModel();
        tb.addColumn("Id Barang");
        tb.addColumn("Nama Barang");
        tb.addColumn("Jumlah");
        tb.addColumn("Harga");
        tb.addColumn("Kategori");
        tb.addColumn("Tanggal Masuk");
        tb.addColumn("Tanggal Keluar");
        jTable1.setModel(tb);
        
        try {
            String sql = "SELECT * FROM barang";
            java.sql.Connection conn = (Connection)Config.configDB();
            java.sql.PreparedStatement pst = conn.prepareStatement(sql);
            java.sql.ResultSet rs = pst.executeQuery();

            while (rs.next()) {
                tb.addRow (new Object[] {
                rs.getString("id_barang"),
                rs.getString("nama"),
                rs.getInt("jumlah"),
                rs.getDouble("harga"),
                rs.getString("kategori"),
                rs.getString("tanggal_masuk"),
                rs.getString("tanggal_keluar"),
                });

            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Gagal menampilkan data: " + e.getMessage());
        }
}
   

    private void btn_hapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_hapusActionPerformed
        // TODO add your handling code here:
        try {

            String nama = txtNama.getText();

            if (!nama.isEmpty()) {
                String sql = "DELETE FROM barang WHERE nama = ?";
                java.sql.Connection conn = (Connection) Config.configDB();
                java.sql.PreparedStatement pst = conn.prepareStatement(sql);
                pst.setString(1, nama); 
                int rowsAffected = pst.executeUpdate(); 

                if (rowsAffected > 0) {
                    JOptionPane.showMessageDialog(null, "Data berhasil dihapus!");


                    clearField();
                    readData();  
                } else {
                    JOptionPane.showMessageDialog(null, "Nama barang tidak ditemukan!");
                }
            } else {
                JOptionPane.showMessageDialog(null, "Nama barang tidak boleh kosong!");
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Gagal menghapus data: " + e.getMessage());
        }

    }//GEN-LAST:event_btn_hapusActionPerformed

